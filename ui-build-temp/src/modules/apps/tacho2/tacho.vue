  for (let k = 0; k < rpmTextRefs.value.length; k++) {
    const el = rpmTextRefs.value[k]
    if (!el) continue
    el.style.visibility = "hidden"
  }
  for (let k = 0; k < tickMarkRefs.value.length; k++) {
    const tl = tickMarkRefs.value[k]
    if (!tl) continue
    tl.style.visibility = "hidden"
  }
}

function update(streams) {
  const isStreamValid = streams.engineInfo && streams.engineInfo[1] !== undefined && streams.engineInfo[1] !== 0 && streams.electrics

  if (!initialized.value) {
    reset()

    if (isStreamValid) {

      // Initialize gauge values using engine max RPM
      const engineMaxRpm = streams.engineInfo[1] || 0
      rpm_max.value = computeGaugeFullRange(engineMaxRpm)

      let upshiftRPM_perc = streams.engineInfo[1] / rpm_max.value
      if (streams.engineInfo[1] == 0) upshiftRPM_perc = 1

      let upshiftRPM2_perc = 1 //((streams.engineInfo[1] - streams.engineInfo[2]) / rpm_max);

      redLineRef.value.style.strokeDasharray = redLineLen.value * upshiftRPM2_perc + " " + redLineLen.value
      redLineRef.value.style.strokeDashoffset = -upshiftRPM_perc * redLineLen.value

      // compute tick marks (remove first and last)
      const totalMarks = computeGaugeMarks(engineMaxRpm) // includes 0 and max
      const interiorMarks = Math.max(0, totalMarks - 2)
      let dashCount = Math.min(interiorMarks, maxRpmTexts)

      let dashes = (revCurveDashesLen.value - dashSize * dashCount) / (dashCount + 1)
      revcurveDashesRef.value.style.strokeDasharray = dashSize + " " + dashes
      revcurveDashesRef.value.style.strokeDashoffset = dashSize
      // use custom tick lines instead of the stroke-dash pattern
      revcurveDashesRef.value.style.display = "none"

      // Place labels and ticks; legend/divisor depend on step size
      const stepRpm = computeGaugeStep(engineMaxRpm)
      if (rpmLegendTextRef?.value) {
        rpmLegendTextRef.value.textContent = stepRpm === 500 ? "x100 RPM" : "x1000 RPM"
      }
      const labelDivisor = stepRpm === 500 ? 100 : 1000
      for (let k = 1; k <= dashCount; k++) {
        const tickRpm = stepRpm * k
        const t = tickRpm / rpm_max.value
        const pos = rpmTextGuideLineRef.value.getPointAtLength(rpmTextGuideLineLen.value * t)

        const rp = rpmTextRefs.value[k]
        if (!rp) continue
        const cx = rp.getAttribute("x")
        const cy = rp.getAttribute("y")
        rp.setAttribute("transform", "translate(" + (pos.x - cx) + "," + (pos.y - cy + rpmTextSize / 2) + ")")
        rp.textContent = (tickRpm / labelDivisor).toString()
        rp.style["visibility"] = "visible"

        // Position tick perpendicular to revcurve
        const line = tickMarkRefs.value[k]
        if (line) {
          // anchor tick to the revcurve path at same t and draw perpendicular to the path
          const clampedLen = Math.max(0, Math.min(revCurveLen.value, revCurveLen.value * t))
          const pt = revcurveRef.value.getPointAtLength(clampedLen)
          const pPrev = revcurveRef.value.getPointAtLength(Math.max(0, clampedLen - 0.5))
          const pNext = revcurveRef.value.getPointAtLength(Math.min(revCurveLen.value, clampedLen + 0.5))
          const tx = pNext.x - pPrev.x
          const ty = pNext.y - pPrev.y
          const tlen = Math.hypot(tx, ty) || 1
          const nx = -ty / tlen
          const ny = tx / tlen
          const cx = width / 2
          const cy = height / 2
          const toCenterX = cx - pt.x
          const toCenterY = cy - pt.y
          const dot = nx * toCenterX + ny * toCenterY
          const sign = dot >= 0 ? 1 : -1
          const inx = nx * sign
          const iny = ny * sign
          // draw tick only inward from the path, with a small inset
          const x1 = pt.x + inx * tickInnerOffset
          const y1 = pt.y + iny * tickInnerOffset
          const x2 = x1 + inx * tickMarkLength
          const y2 = y1 + iny * tickMarkLength
          line.setAttribute("x1", x1)
          line.setAttribute("y1", y1)
          line.setAttribute("x2", x2)
          line.setAttribute("y2", y2)
          line.style["visibility"] = "visible"
        }
      }
      // hide any remaining preallocated labels
      for (let k = dashCount + 1; k <= maxRpmTexts; k++) {
        const rp = rpmTextRefs.value[k]
        if (rp) rp.style["visibility"] = "hidden"
        const line = tickMarkRefs.value[k]
        if (line) line.style["visibility"] = "hidden"
      }

      initialized.value = true
    }
  }

  if (!isStreamValid) {
    return false
  }

  setlayersVisible(true)

  if (displayMode.value == 2) {
    // real data mode, no animation
    if (streams.electrics.wheelspeed) {
      data.speedtext = UnitSpeed(streams.electrics.wheelspeed)
      if (streams.electrics.wheelspeed > 9000) {
        speedUnitTextRef.value.textContent = "brrrr"
      }
    } else if (streams.electrics.airspeed) {
      data.speedtext = UnitSpeed(streams.electrics.airspeed)
    }
    (function () {
      if (streams.engineInfo[13] == "manual") {
        let gear = streams.engineInfo[5]
        let gearStr = gear.toString()

        if (gear == 0) gearStr = "N"
        else if (gear == -1) gearStr = "R"
        else if (-gear > 1) gearStr = "R" + -gear

        data.geartext = gearStr
      } else {
        data.geartext = ["P", "R", "N", "D", "2", "1"][Math.round(streams.electrics.gear_A * 5)]
      }
    })()

    data.fuel = streams.engineInfo[11] / streams.engineInfo[12]
    data.parkingBrake = streams.electrics.parkingbrake
    data.absWorking = streams.electrics.abs
    data.signalL = streams.electrics.signal_L
    data.signalR = streams.electrics.signal_R
    data.waterTemp = streams.electrics.watertemp
    data.lowBeam = streams.electrics.lowbeam
    data.highBeam = streams.electrics.highbeam
    data.rpm = (streams.electrics.rpmTacho || 0.0) / rpm_max.value
  } else if (displayMode.value == 0) {
    // starting animation
    testVar = testVar + 0.04
    if (testVar > 1) testVar = 1
    data.speedtext = Math.round(testVar * 100)
    data.geartext = Math.round(testVar * 5)
    data.fuel = testVar
    let boolTest = true //Math.round(testVar * 10) % 10;
    data.parkingBrake = boolTest
    data.absWorking = boolTest
    data.signalL = boolTest
    data.signalR = boolTest
    data.oilTemp = testVar
    data.lowBeam = boolTest
    data.highBeam = !boolTest
    data.rpm = testVar
    if (testVar >= 1) {
      testVar = 0
      displayMode.value = 1
    }
  } else if (displayMode.value == 1) {
    // starting animation -> real data transition

    if (streams.electrics.wheelspeed) {
      data.speedtext = UnitSpeed(streams.electrics.wheelspeed)
    } else {
      data.speedtext = ""
      speedUnitTextRef.value.textContent = ""
    }

    (function () {
      let gear = streams.engineInfo[5]
      let gearStr = gear.toString()
      if (gear == 0) {
        gearStr = "N"
      } else if (gear == -1) {
        gearStr = "R"
      }
      data.geartext = gearStr
    })()

    data.parkingBrake = streams.electrics.parkingbrake
    data.absWorking = streams.electrics.abs
    data.signalL = streams.electrics.signal_L
    data.signalR = streams.electrics.signal_R
    data.lowBeam = streams.electrics.lowbeam
    data.highBeam = streams.electrics.highbeam

    let oilok = Math.abs(data.oilTemp - streams.electrics.oiltemp) < 0.005
    if (!oilok) data.oilTemp += (streams.electrics.oiltemp - data.oilTemp) * 0.2

    let rpmperc = streams.electrics.rpm / rpm_max.value
    let rpmok = Math.abs(data.rpm - rpmperc) < 0.005
    if (!rpmok) data.rpm += (rpmperc - data.rpm) * 0.2

    let fuelperc = streams.engineInfo[11] / streams.engineInfo[12]
    let fuelok = Math.abs(data.fuel - fuelperc) < 0.005
    if (!fuelok) data.fuel += (fuelperc - data.fuel) * 0.2

    if (oilok && rpmok && fuelok) {
      displayMode.value = 2
    }
  }

  data.engineRunning = streams.electrics.engineRunning
  data.ignition = streams.electrics.ignition
  applyData(data)
  return true
}

