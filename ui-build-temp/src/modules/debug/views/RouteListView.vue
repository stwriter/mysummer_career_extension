<template>
	<div class="routes-list">
		<h1>Vue Routes List</h1>
		<p>Click to visit:</p>
		<ul>
			<li v-for="route in routes" :key="route.name">
				<BngButton @click="go(route)" tabindex="1">{{ route.name }}</BngButton>
			</li>
		</ul>
	</div>
</template>

<script setup>
import { BngButton } from "@/common/components/base"
import router from "@/router"

const routes = router
	.getRoutes()
	.filter(r => r.name !== "routelist")
	.sort((a, b) => a.name.localeCompare(b.name))

function go(route) {
	router.push(route)
}
</script>

